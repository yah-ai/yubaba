/// Local-tier combined test: all three public-ingress sub-cases.
///
/// Runs without a real containerd or cloudflared daemon — uses `FakeRuntime`
/// and an in-process `CloudflaredMock`. Always runs in CI.
#[tokio::test]
#[allow(non_snake_case)]
async fn public_ingress__local() {
    case_with_mock().await;
    case_port_mapping_fallback().await;
    case_tunnel_failure().await;
}

